"""
Modulo per l'associazione dati (data association) in sistemi di Multi-Object Tracking (MOT).

Questo script implementa un insieme di funzioni fondamentali per l'associazione tra le tracce 
esistenti (oggetti già tracciati) e le nuove rilevazioni ottenute da un detector, ad esempio 
volti rilevati da YuNet in una videochiamata o persone rilevate in un video.

L'associazione è una fase critica nei sistemi di tracking, poiché consente di mantenere la 
coerenza temporale delle identità lungo una sequenza video. Questo è particolarmente utile 
nei contesti di videosorveglianza, analisi del comportamento, o — come nel caso di questa tesi — 
come fase di pre-processing per il rilevamento di deepfake in streaming da webcam.

Le principali funzionalità di questo modulo includono:

1. **Calcolo di metriche di similarità**:
   - *IoU (Intersection over Union)* tra bounding box, usata per stimare l'overlap tra oggetti;
   - *Distanze tra vettori di embedding* (es. cosine distance), utili quando si ha accesso a feature visive;
   - *Distanze di Mahalanobis* basate su predizione di Kalman Filter, per valutare coerenza spaziale/temporale.

2. **Strategie di fusione multi-costo**:
   Le funzioni `fuse_motion`, `fuse_iou`, e `fuse_score` permettono di combinare diverse fonti di informazione
   (es. re-ID + motion + confidenza del detector) in una singola matrice di costo per l'assegnamento.

3. **Filtraggio tramite gating**:
   Utilizza le proprietà statistiche della distribuzione Chi-Quadrato per escludere associazioni "improbabili"
   rispetto alla predizione di stato del Kalman Filter. Questo migliora la robustezza del sistema.

4. **Assegnamento ottimo tramite algoritmo LAPJV**:
   L'algoritmo LAPJV (Jonker-Volgenant) risolve il problema di assegnamento lineare con vincoli di costo.
   Questo è implementato nella funzione `linear_assignment`, che garantisce un matching ottimo tra tracce e detections.

5. **Supporto a matching a più livelli**:
   Funzione `merge_matches` utile in sistemi che usano associazioni su più livelli o frame (es. cascaded matching).

L'intero modulo è pensato per essere integrato in un sistema più ampio di tracking (es. ByteTrack),
e rappresenta una componente cruciale nella pipeline di tracciamento, poiché consente di mantenere
l'identità persistente degli oggetti nel tempo, anche in presenza di rumore, occlusioni, o falsi positivi.

Questo approccio è estremamente utile in applicazioni real-time come il monitoraggio da webcam
in tempo reale per la rilevazione di deepfake, dove è importante non solo rilevare il volto
ma anche tracciare la sua posizione coerente nel tempo per estrarre sequenze video significative e affidabili.
"""


def merge_matches(m1, m2, shape):
    O,P,Q = shape
    m1 = np.asarray(m1)
    m2 = np.asarray(m2)

    M1 = scipy.sparse.coo_matrix((np.ones(len(m1)), (m1[:, 0], m1[:, 1])), shape=(O, P))
    M2 = scipy.sparse.coo_matrix((np.ones(len(m2)), (m2[:, 0], m2[:, 1])), shape=(P, Q))

    mask = M1*M2
    match = mask.nonzero()
    match = list(zip(match[0], match[1]))
    unmatched_O = tuple(set(range(O)) - set([i for i, j in match]))
    unmatched_Q = tuple(set(range(Q)) - set([j for i, j in match]))

    return match, unmatched_O, unmatched_Q


def _indices_to_matches(cost_matrix, indices, thresh):
    matched_cost = cost_matrix[tuple(zip(*indices))]
    matched_mask = (matched_cost <= thresh)

    matches = indices[matched_mask]
    unmatched_a = tuple(set(range(cost_matrix.shape[0])) - set(matches[:, 0]))
    unmatched_b = tuple(set(range(cost_matrix.shape[1])) - set(matches[:, 1]))

    return matches, unmatched_a, unmatched_b


def linear_assignment(cost_matrix, thresh):
    if cost_matrix.size == 0:
        return np.empty((0, 2), dtype=int), tuple(range(cost_matrix.shape[0])), tuple(range(cost_matrix.shape[1]))
    matches, unmatched_a, unmatched_b = [], [], []
    cost, x, y = lap.lapjv(cost_matrix, extend_cost=True, cost_limit=thresh)
    for ix, mx in enumerate(x):
        if mx >= 0:
            matches.append([ix, mx])
    unmatched_a = np.where(x < 0)[0]
    unmatched_b = np.where(y < 0)[0]
    matches = np.asarray(matches)
    return matches, unmatched_a, unmatched_b


def ious(atlbrs, btlbrs):
    """
    Compute cost based on IoU
    :type atlbrs: list[tlbr] | np.ndarray
    :type atlbrs: list[tlbr] | np.ndarray

    :rtype ious np.ndarray
    """
    ious = np.zeros((len(atlbrs), len(btlbrs)), dtype=float)
    if ious.size == 0:
        return ious

    ious = bbox_ious(
        np.ascontiguousarray(atlbrs, dtype=float),
        np.ascontiguousarray(btlbrs, dtype=float)
    )

    return ious


def iou_distance(atracks, btracks):
    """
    Compute cost based on IoU
    :type atracks: list[STrack]
    :type btracks: list[STrack]

    :rtype cost_matrix np.ndarray
    """

    if (len(atracks)>0 and isinstance(atracks[0], np.ndarray)) or (len(btracks) > 0 and isinstance(btracks[0], np.ndarray)):
        atlbrs = atracks
        btlbrs = btracks
    else:
        atlbrs = [track.tlbr for track in atracks]
        btlbrs = [track.tlbr for track in btracks]
    _ious = ious(atlbrs, btlbrs)
    cost_matrix = 1 - _ious

    return cost_matrix

